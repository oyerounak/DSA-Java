public class IdentifiersInJava {
    public static void main(String[] args) {
        final int var1 = 99;
        double var2 = 2.0;
        System.out.println("Hello World!");
        // Identifiers List in this program

        // 1.IdentifiersInJava (Class name)
        // 2.main (Method name)
        // 3.String (Predefined Class name)
        // 4.args (String variable name)
        // 5.var1 (integer variable name)
        // 6.var2 (double variable name)
        // 7.System(Predefined Class name)
        // 8.out(Variable name)
        // 9.println (Method name)
    }
    
}
