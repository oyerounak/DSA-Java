public class LeftHalfPyramid {
    public static void main(String[] args) {
        /*
    
         * 
         * *
         * * *
         * * * *
         * * * * *
        
         */
        int n =5;
        int i = 1;
        for (i =1; i<=n;i++) {
            int j =1;
            for(j=1;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println();
            
        }
    }
    
}
